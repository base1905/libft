#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	long int	b_address;

	b_address = (long int)b;
	while (len > 0)
	{
		((char *)b_address)[0] = (unsigned char)c;
		b_address += 1;
		len -= 1;
	}
	return (b);
}
